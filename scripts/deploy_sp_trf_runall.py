sp_runall_sql = f"""
CREATE OR REPLACE PROCEDURE SF_SP_RUN_ALL_PLANS(
    START_WEEK_ID FLOAT, END_WEEK_ID FLOAT,
    MAJ_CAT_PARAM VARCHAR DEFAULT NULL,
    COVER_DAYS_CM1 FLOAT DEFAULT 14, COVER_DAYS_CM2 FLOAT DEFAULT 0
)
RETURNS VARIANT
LANGUAGE SQL
EXECUTE AS CALLER
AS
DECLARE
    v_trf INTEGER;
    v_pp  INTEGER;
    v_ts  TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP();
BEGIN
    -- Truncate both output tables
    TRUNCATE TABLE IF EXISTS TRF_IN_PLAN;
    TRUNCATE TABLE IF EXISTS PURCHASE_PLAN;

    -- Generate TRF (all stores, optional MAJ_CAT filter)
    CALL SF_SP_GENERATE_TRF_IN_PLAN(:START_WEEK_ID, :END_WEEK_ID, NULL, :MAJ_CAT_PARAM, :COVER_DAYS_CM1, :COVER_DAYS_CM2);
    v_trf := (SELECT COUNT(*) FROM TRF_IN_PLAN);

    -- Generate Purchase Plan
    IF (:MAJ_CAT_PARAM IS NULL) THEN
        CALL SF_SP_GENERATE_PURCHASE_PLAN(:START_WEEK_ID, :END_WEEK_ID, NULL, NULL);
    ELSE
        CALL SF_SP_GENERATE_PURCHASE_PLAN(:START_WEEK_ID, :END_WEEK_ID, NULL, :MAJ_CAT_PARAM);
    END IF;
    v_pp := (SELECT COUNT(*) FROM PURCHASE_PLAN);

    RETURN OBJECT_CONSTRUCT(
        'trf_rows', :v_trf,
        'pp_rows', :v_pp,
        'total_seconds', DATEDIFF('second', :v_ts, CURRENT_TIMESTAMP())
    );
END;
"""

